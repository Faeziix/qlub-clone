const dicts: Record<string, Dict> = { fa, en };

export function getDict(locale: string): Dict {
  return dicts[locale] ?? en;
}

/** Server + client safe translator. Falls back to English then key. */
export function makeT(locale: string) {
  const d = getDict(locale);
  return (key: keyof typeof en | string): string =>
    d[key as string] ?? en[key as string] ?? (key as string);
}
